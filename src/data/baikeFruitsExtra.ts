/** 将紧凑定义转换为 BaikeInfo 格式 */
function buildBaikeInfo(def: FruitDef): BaikeInfo {
  const info: BaikeInfo = {
    title: def.name,
    summary: def.summary,
    content: def.content,
  };
  if (def.sections.length > 0) {
    info.sections = def.sections.map((s): BaikeSection => ({
      title: s.title,
      paragraphs: s.paragraphs,
    }));
  }
  if (def.infobox && Object.keys(def.infobox).length > 0) {
    info.infobox = def.infobox;
  }
  info.image = '';
  return info;
}

/** 内置水果百科扩充数据 */
export const builtinFruitBaikeExtra: Record<string, BaikeInfo> = (() => {
  const result: Record<string, BaikeInfo> = {};
  for (const def of FRUIT_DEFS) {
    result[def.name.toLowerCase()] = buildBaikeInfo(def);
  }
  return result;
})();
