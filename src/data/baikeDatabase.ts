// 水果百科（扩展字段）
export interface FruitBaike {
  name: string; // 水果名称
  emoji: string; // 水果图标 emoji
  potassiumPer100g: number; // 每 100g 钾含量 (mg)
  phosphorusPer100g: number; // 每 100g 磷含量 (mg)
  sodiumPer100g: number; // 每 100g 钠含量 (mg)
  waterPer100g: number; // 每 100g 水分含量 (ml/g)
  aliases?: string; // 别名
  description: string; // 简介
  origin: string; // 起源与分布
  varieties: string; // 主要品种
  cultivation: string; // 栽培与生产
  culture: string; // 文化与历史
  healthBenefits: string; // 健康益处
  precautions: string; // 食用禁忌与注意事项
  storage: string; // 保存方法
  image?: string; // 图片 URL（可选）
}

// 药物百科（扩展字段）
export interface MedicationBaike {
  name: string; // 药物名称
  emoji: string; // 药物图标 emoji
  category: MedicationCategory; // 药物分类
  description: string; // 简介
  indications: string; // 适应症
  pharmacokinetics: string; // 药代动力学
  contraindications: string; // 禁忌症
  sideEffects: string; // 不良反应
  warnings: string; // 注意事项
  useInPregnancy: string; // 孕妇及哺乳期妇女用药
  useInChildren: string; // 儿童用药
  useInElderly: string; // 老年患者用药
  drugInteractions: string; // 药物相互作用
  storage: string; // 贮藏
  overdose: string; // 药物过量
  image?: string; // 图片 URL（可选）
}

